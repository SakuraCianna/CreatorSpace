-- CreatorSpace PostgreSQL 建库初始化片段。
-- Flyway 连接到目标库后执行本迁移，PostgreSQL 不允许在目标库事务内再执行 create database。
-- 首次部署时请先在 postgres 管理库手动执行以下建库语句，再启动后端迁移：
-- create role creatorspace with login password 'change_me';
-- create database creatorspace with owner creatorspace encoding 'UTF8';
-- grant all privileges on database creatorspace to creatorspace;

create extension if not exists pgcrypto;
create extension if not exists pg_trgm;
create extension if not exists vector;

do $$
begin
    execute format(
        'comment on database %I is %L',
        current_database(),
        'CreatorSpace 个人主题博客与创意作品展示平台数据库'
    );
end $$;

comment on schema public is 'CreatorSpace 业务数据 schema';

create table roles (
    id bigint generated by default as identity primary key,
    code varchar(40) not null unique,
    name varchar(80) not null,
    description text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table users (
    id bigint generated by default as identity primary key,
    username varchar(64) not null unique,
    nickname varchar(80),
    password_hash varchar(255) not null,
    avatar_url text,
    bio text,
    status varchar(20) not null default 'ACTIVE',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    last_login_at timestamptz,
    constraint ck_users_status check (status in ('ACTIVE', 'DISABLED', 'LOCKED'))
);

create table user_roles (
    user_id bigint not null references users(id) on delete cascade,
    role_id bigint not null references roles(id) on delete cascade,
    created_at timestamptz not null default now(),
    primary key (user_id, role_id)
);

create table user_friendships (
    id bigint generated by default as identity primary key,
    requester_id bigint not null references users(id) on delete cascade,
    addressee_id bigint not null references users(id) on delete cascade,
    status varchar(20) not null default 'PENDING',
    requested_at timestamptz not null default now(),
    accepted_at timestamptz,
    updated_at timestamptz not null default now(),
    constraint ck_user_friendships_status check (status in ('PENDING', 'ACCEPTED', 'REJECTED', 'BLOCKED')),
    constraint ck_user_friendships_not_self check (requester_id <> addressee_id),
    unique (requester_id, addressee_id)
);

create index idx_user_friendships_requester on user_friendships(requester_id, status);
create index idx_user_friendships_addressee on user_friendships(addressee_id, status);

create table categories (
    id bigint generated by default as identity primary key,
    module varchar(30) not null,
    name varchar(80) not null,
    slug varchar(120) not null,
    description text,
    sort_order integer not null default 0,
    enabled boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_categories_module check (module in ('ARTICLE', 'PROJECT', 'INSPIRATION'))
);

create unique index uk_categories_module_slug on categories(module, slug);

create table tags (
    id bigint generated by default as identity primary key,
    name varchar(80) not null,
    slug varchar(120) not null unique,
    color varchar(32),
    weight integer not null default 0,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table articles (
    id bigint generated by default as identity primary key,
    title varchar(200) not null,
    slug varchar(220) not null unique,
    summary text,
    content_markdown text not null default '',
    cover_url text,
    category_id bigint references categories(id) on delete set null,
    status varchar(20) not null default 'DRAFT',
    privacy_type varchar(20) not null default 'PUBLIC',
    view_count bigint not null default 0,
    like_count bigint not null default 0,
    comment_count bigint not null default 0,
    is_top boolean not null default false,
    is_recommend boolean not null default false,
    publish_time timestamptz,
    created_by bigint references users(id) on delete set null,
    updated_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_articles_status check (status in ('DRAFT', 'PUBLISHED', 'PRIVATE', 'SCHEDULED', 'ARCHIVED')),
    constraint ck_articles_privacy_type
        check (privacy_type in ('PUBLIC', 'SELF', 'FRIENDS', 'SELECTED_FRIENDS', 'EXCLUDED_FRIENDS'))
);

create index idx_articles_category_id on articles(category_id);
create index idx_articles_publish_time on articles(publish_time desc);
create index idx_articles_title_trgm on articles using gin (title gin_trgm_ops);
create index idx_articles_summary_trgm on articles using gin (summary gin_trgm_ops);
create index idx_articles_content_trgm on articles using gin (content_markdown gin_trgm_ops);

create table article_visibility_users (
    article_id bigint not null references articles(id) on delete cascade,
    user_id bigint not null references users(id) on delete cascade,
    rule_type varchar(20) not null,
    created_at timestamptz not null default now(),
    primary key (article_id, user_id),
    constraint ck_article_visibility_users_rule_type check (rule_type in ('ALLOW', 'DENY'))
);

create index idx_article_visibility_users_user on article_visibility_users(user_id, rule_type);

create table article_versions (
    id bigint generated by default as identity primary key,
    article_id bigint not null references articles(id) on delete cascade,
    version_no integer not null,
    title varchar(200) not null,
    summary text,
    content_markdown text not null,
    created_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    unique (article_id, version_no)
);

create table article_tags (
    article_id bigint not null references articles(id) on delete cascade,
    tag_id bigint not null references tags(id) on delete cascade,
    primary key (article_id, tag_id)
);

create table portfolio_projects (
    id bigint generated by default as identity primary key,
    title varchar(200) not null,
    slug varchar(220) not null unique,
    description text,
    cover_url text,
    project_type varchar(60) not null,
    tech_stack jsonb not null default '[]'::jsonb,
    github_url text,
    demo_url text,
    video_url text,
    content_markdown text not null default '',
    status varchar(20) not null default 'VISIBLE',
    is_recommend boolean not null default false,
    sort_order integer not null default 0,
    started_at date,
    ended_at date,
    created_by bigint references users(id) on delete set null,
    updated_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_portfolio_projects_status check (status in ('VISIBLE', 'HIDDEN', 'DRAFT', 'ARCHIVED'))
