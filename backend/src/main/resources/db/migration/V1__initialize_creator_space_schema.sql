create table comments (
    id bigint generated by default as identity primary key,
    target_type varchar(30) not null,
    target_id bigint not null,
    parent_id bigint references comments(id) on delete cascade,
    root_id bigint references comments(id) on delete cascade,
    user_id bigint not null references users(id) on delete cascade,
    reply_to_user_id bigint references users(id) on delete set null,
    content text not null,
    status varchar(20) not null default 'PENDING',
    depth integer not null default 0,
    reply_count bigint not null default 0,
    like_count bigint not null default 0,
    ip_address inet,
    user_agent text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_comments_target_type check (target_type in ('ARTICLE', 'PROJECT', 'MESSAGE')),
    constraint ck_comments_status check (status in ('PENDING', 'APPROVED', 'REJECTED', 'SPAM')),
    constraint ck_comments_depth check (depth >= 0)
);

create index idx_comments_target on comments(target_type, target_id);
create index idx_comments_parent_id on comments(parent_id);
create index idx_comments_root_id on comments(root_id);
create index idx_comments_user_id on comments(user_id);
create index idx_comments_status_created_at on comments(status, created_at desc);

create table sensitive_words (
    id bigint generated by default as identity primary key,
    word varchar(120) not null unique,
    match_type varchar(20) not null default 'CONTAINS',
    severity varchar(20) not null default 'REVIEW',
    enabled boolean not null default true,
    created_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_sensitive_words_match_type check (match_type in ('EXACT', 'CONTAINS', 'REGEX')),
    constraint ck_sensitive_words_severity check (severity in ('REVIEW', 'REJECT', 'MASK'))
);

create index idx_sensitive_words_enabled on sensitive_words(enabled);

