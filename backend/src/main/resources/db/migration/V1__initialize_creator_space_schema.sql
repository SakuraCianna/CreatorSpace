create table inspiration_cards (
    id bigint generated by default as identity primary key,
    title varchar(200) not null,
    content text,
    image_url text,
    card_type varchar(30) not null,
    source_url text,
    color varchar(32),
    is_public boolean not null default true,
    sort_order integer not null default 0,
    created_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint ck_inspiration_cards_type check (card_type in ('IMAGE', 'TEXT', 'PROMPT', 'CODE', 'LINK'))
);

create index idx_inspiration_cards_title_trgm on inspiration_cards using gin (title gin_trgm_ops);
create index idx_inspiration_cards_content_trgm on inspiration_cards using gin (content gin_trgm_ops);

create table inspiration_tags (
    inspiration_id bigint not null references inspiration_cards(id) on delete cascade,
    tag_id bigint not null references tags(id) on delete cascade,
    primary key (inspiration_id, tag_id)
