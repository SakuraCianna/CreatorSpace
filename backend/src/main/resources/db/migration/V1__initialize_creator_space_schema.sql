    id bigint generated by default as identity primary key,
    config_key varchar(120) not null unique,
    config_value jsonb not null,
    description text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table file_resources (
    id bigint generated by default as identity primary key,
    file_name varchar(255) not null,
    original_name varchar(255) not null,
    relative_path text not null,
    public_url text not null,
    file_type varchar(120) not null,
    file_size bigint not null,
    storage_type varchar(30) not null default 'LOCAL',
    module varchar(40) not null,
    created_by bigint references users(id) on delete set null,
    created_at timestamptz not null default now(),
    constraint ck_file_resources_storage_type check (storage_type in ('LOCAL', 'MINIO', 'S3')),
    constraint ck_file_resources_module check (module in ('AVATAR', 'COVER', 'ARTICLE', 'PROJECT', 'INSPIRATION', 'OTHER'))
);

create table file_resource_references (
    id bigint generated by default as identity primary key,
    file_id bigint not null references file_resources(id) on delete cascade,
    target_type varchar(40) not null,
    target_id bigint not null,
    usage_type varchar(40) not null,
    created_at timestamptz not null default now(),
    constraint ck_file_resource_references_target_type
        check (target_type in ('USER', 'ARTICLE', 'PROJECT', 'INSPIRATION', 'COMMENT', 'SITE_CONFIG', 'THEME')),
    constraint ck_file_resource_references_usage_type
        check (usage_type in ('AVATAR', 'COVER', 'CONTENT', 'ATTACHMENT', 'SCREENSHOT', 'CONFIG'))
);

create unique index uk_file_resource_references
    on file_resource_references(file_id, target_type, target_id, usage_type);
create index idx_file_resource_references_target
    on file_resource_references(target_type, target_id);

